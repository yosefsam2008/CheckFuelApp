/**
 * Get known engine CCs for a brand-model combination
 * Uses fuzzy matching: if exact match not found, tries partial matches
 *
 * @param brand - Brand name (Hebrew or English)
 * @param model - Model name (case-insensitive)
 * @returns Array of known CC values, or undefined if not found
 *
 * @example
 * getKnownEngineCCs('Honda', 'CIVIC') // [1497, 1799, 1996]
 * getKnownEngineCCs('Honda', 'CIVIC TOURER') // [1497, 1799, 1996] (fuzzy match to CIVIC)
 * getKnownEngineCCs('הונדה', 'civic') // [1497, 1799, 1996]
 */
export function getKnownEngineCCs(brand: string, model: string): number[] | undefined {
  const normalizedBrand = translateBrandToEnglish(brand);
  const normalizedModel = model.toUpperCase().trim();

  const brandModels = KNOWN_ENGINES[normalizedBrand];
  if (!brandModels) return undefined;

  // Strategy 1: Exact match
  if (brandModels[normalizedModel]) {
    return brandModels[normalizedModel];
  }

  // Strategy 2: Try first word only (e.g., "CIVIC TOURER" -> "CIVIC")
  const firstWord = normalizedModel.split(/[\s-]+/)[0];
  if (firstWord && firstWord.length >= 2 && brandModels[firstWord]) {
    return brandModels[firstWord];
  }

  // Strategy 3: Find models that start with the search term
  const startsWithMatches = Object.keys(brandModels).filter(m =>
    m.startsWith(normalizedModel) || normalizedModel.startsWith(m)
  );
  if (startsWithMatches.length > 0) {
    // Combine all CC values from matching models
    const allCCs = new Set<number>();
    for (const match of startsWithMatches) {
      brandModels[match].forEach(cc => allCCs.add(cc));
    }
    return Array.from(allCCs).sort((a, b) => a - b);
  }

  // Strategy 4: Find models containing the first word
  if (firstWord && firstWord.length >= 3) {
    const containsMatches = Object.keys(brandModels).filter(m =>
      m.includes(firstWord) || firstWord.includes(m)
    );
    if (containsMatches.length > 0) {
      const allCCs = new Set<number>();
      for (const match of containsMatches) {
        brandModels[match].forEach(cc => allCCs.add(cc));
      }
      return Array.from(allCCs).sort((a, b) => a - b);
    }
  }

  return undefined;
}

